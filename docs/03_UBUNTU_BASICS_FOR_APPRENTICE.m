# Ubuntu Basics for the Apprentice Engineer

This document teaches the apprentice how to operate the Ubuntu desktop environment.  
These skills are required before using the Terminal, VS Code, or the compiler.

Follow each step exactly.

---

# 1. The Desktop

Ubuntu has:

- a top bar  
- a left‑side launcher  
- a workspace background  

Operator rule:

- The apprentice does not rearrange the desktop.
- The default layout is correct.

---

# 2. The Super Key

The **Super** key (Windows logo key) opens the system launcher.

Press:

```
Super
```

Type anything to search.

Examples:

```
Terminal
Visual Studio Code
Files
Settings
```

Press **ENTER** to open the selected item.

---

# 3. Open the Terminal

Press:

```
Super
```

Type:

```
Terminal
```

Press **ENTER**.

This opens the Ubuntu Terminal.

---

# 4. Open the File Manager

Press:

```
Super
```

Type:

```
Files
```

Press **ENTER**.

This opens the file browser.

Operator rule:

- The apprentice uses the file browser only for navigation, not for editing.

---

# 5. Create Folders (Graphical)

In the file browser:

1. Right‑click inside a folder.  
2. Select **New Folder**.  
3. Type the folder name.  
4. Press **ENTER**.

Operator rule:

- The apprentice creates project folders only through operator scripts, not manually.

---

# 6. Rename Files (Graphical)

1. Right‑click the file.  
2. Select **Rename**.  
3. Type the new name.  
4. Press **ENTER**.

Operator rule:

- The apprentice renames only files they created.

---

# 7. Delete Files (Graphical)

1. Right‑click the file.  
2. Select **Move to Trash**.

Operator rule:

- The apprentice deletes only files they created.

---

# 8. Switch Windows

Use:

```
Alt + Tab
```

Hold **Alt**, tap **Tab** to cycle through windows.

---

# 9. Close Windows

Press:

```
Ctrl + Q
```

This closes the active application.

---

# 10. Shutdown and Restart

Open the system menu (top‑right corner).

Choose:

- **Power Off / Log Out**  
- **Restart**  
- **Shut Down**

Operator rule:

- Always shut down cleanly.  
- Never force power off.

---

# 11. Completion

When the apprentice can:

- open applications  
- search with Super  
- navigate files  
- create folders  
- rename files  
- delete files  
- switch windows  
- close applications  
- shut down correctly  

…they have the required Ubuntu foundation for all future operator work.

